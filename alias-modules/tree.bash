# Shortcut for tree -C
alias t='tree -C'

##
# Show tree with colour output
alias tree='tree -C'

##
# Shortcut for tree all colour
alias ta='tree -Ca'

##
# Shortcut for tree all directories
alias tad='tree -CqNpughDd'

##
# Shortcut for tree all list
alias tal='tree -CqNpughDa'

##
# Shortcut for tree directories only
alias td='tree -Cd'

##
# Shortcut for tree list
alias tl='tree -CqNpughD'

